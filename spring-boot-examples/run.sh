# Spring Boot 快速启动脚本

## 使用说明

本脚本帮助你快速启动各个Spring Boot示例应用。

## 运行方式

```bash
chmod +x run.sh
./run.sh [示例名称]
```

## 可用示例

- `quickstart` - Spring Boot 快速入门
- `restful` - RESTful API 实战
- `jpa` - Spring Data JPA 实战

## 示例

```bash
# 运行快速入门示例
./run.sh quickstart

# 运行RESTful API示例
./run.sh restful

# 运行JPA示例
./run.sh jpa
```
