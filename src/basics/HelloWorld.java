package basics;

/**
 * 第一个Java程序 - Hello World
 * 学习目标：
 * 1. 理解Java程序的基本结构
 * 2. 理解main方法的作用
 * 3. 学习如何输出信息到控制台
 */
public class HelloWorld {
    public static void main(String[] args) {
        // 打印欢迎信息
        System.out.println("欢迎来到Java的世界！");
        System.out.println("Hello, World!");

        // 打印一些有趣的信息
        System.out.println("Java是一门面向对象的编程语言");
        System.out.println("让我们开始学习Java吧！");
    }
}
